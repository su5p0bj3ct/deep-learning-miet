def train_one_stage(model, model_ema, optimizer, scheduler, train_loader, val_loader,
                    epochs, stage_name, fold, start_epoch=0, initial_best_f1=0.0):
    scaler = GradScaler('cuda')
    best_f1 = initial_best_f1
    swa_model = AveragedModel(model) if stage_name == "STAGE2" else None
    swa_scheduler = SWALR(optimizer, swa_lr=1e-6) if swa_model else None
    epochs_no_improve = 0

    for epoch in range(start_epoch, epochs):
        model.train()
        pbar = tqdm(train_loader, desc=f"Fold {fold} | {stage_name} Ep {epoch+1}/{epochs}", leave=False)
        optimizer.zero_grad()

        # Обновляем LR scheduler от timm в начале эпохи (для WarmUp)
        if isinstance(scheduler, CosineLRScheduler):
            scheduler.step(epoch)

        for i, (x, y) in enumerate(pbar):
            x, y = x.to(cfg.DEVICE), y.to(cfg.DEVICE)

            if stage_name == "STAGE2" and random.random() > 0.5:
                if random.random() > 0.5:
                    x, y_a, y_b, lam = mixup_data(x, y)
                else:
                    x, y_a, y_b, lam = cutmix_data(x, y)
            else:
                y_a = y_b = y
                lam = 1.0

            with autocast(device_type=cfg.DEVICE.type):
                logits = model(x)
                loss = lam * loss_fn(logits, y_a) + (1 - lam) * loss_fn(logits, y_b)
                loss = loss / cfg.ACCUM_STEPS

            scaler.scale(loss).backward()

            if (i + 1) % cfg.ACCUM_STEPS == 0:
                scaler.unscale_(optimizer)
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                scaler.step(optimizer)
                scaler.update()
                optimizer.zero_grad()
                if model_ema: model_ema.update(model)

            pbar.set_postfix(loss=loss.item() * cfg.ACCUM_STEPS)

        # Валидация (используем EMA-модель, если она есть)
        val_model = model_ema.module if model_ema else model
        f1_w = evaluate(val_model, val_loader)

        if f1_w > best_f1:
            best_f1 = f1_w
            save_best(val_model, f1_w, epoch, fold)
            epochs_no_improve = 0
        else:
            epochs_no_improve += 1

        # Обновляем стандартный PyTorch scheduler, если это Stage 1
        if not isinstance(scheduler, CosineLRScheduler) and scheduler:
            scheduler.step()

        if swa_model and epoch >= epochs - 5:
            swa_model.update_parameters(model)
            if swa_scheduler: swa_scheduler.step()

        print(f"Fold {fold} | {stage_name} Ep {epoch+1} | Val F1_w = {f1_w:.5f} | Best = {best_f1:.5f}")

        if epochs_no_improve >= cfg.PATIENCE:
            print("Early stopping triggered")
            break

    if swa_model:
        torch.optim.swa_utils.update_bn(train_loader, swa_model)
        save_best(swa_model.module, best_f1, "SWA", fold)
        return swa_model.module

    return model

