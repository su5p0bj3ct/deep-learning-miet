def parse_checkpoint_name(path):
    filename = os.path.basename(path)
    match = re.search(r"fold(\d+)_epoch(\d+)_f1([\d.]+)\.pth", filename)
    if match:
        return {
            "fold": int(match.group(1)),
            "epoch": int(match.group(2)),
            "f1": float(match.group(3))
        }
    return None

